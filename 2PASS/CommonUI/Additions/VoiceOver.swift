import UIKit

enum VoiceOver {
    static func say(_ str: String) {
        if UIAccessibility.isVoiceOverRunning {
            DispatchQueue.main.asyncAfter(deadline: .now() + 0.1) {
                UIAccessibility.post(notification: .announcement, argument: str)
            }
        }
    }
}
